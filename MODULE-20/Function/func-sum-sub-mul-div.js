function add(a, b) {
    var sum = a + b;
    return sum;
}
var num1 = add(555, 45);
var num2 = add(885, 545);
var sum = add(num1, num2);
console.log('Your first Number is: ', num1);
console.log('Your second Number is: ', num2);
console.log(num1, '+', num2, '=', sum);




